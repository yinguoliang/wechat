package com.my.weichat.domain;

public class BeauticianWorks {
	private int worksID;
	private int beauticianID;
	private String worksImg;
	private String worksRemark;
	private String worksDuration;
	private String worksSpendTime;
	public int getWorksID() {
		return worksID;
	}
	public void setWorksID(int worksID) {
		this.worksID = worksID;
	}
	public int getBeauticianID() {
		return beauticianID;
	}
	public void setBeauticianID(int beauticianID) {
		this.beauticianID = beauticianID;
	}
	public String getWorksImg() {
		return worksImg;
	}
	public void setWorksImg(String worksImg) {
		this.worksImg = worksImg;
	}
	public String getWorksRemark() {
		return worksRemark;
	}
	public void setWorksRemark(String worksRemark) {
		this.worksRemark = worksRemark;
	}
	public String getWorksDuration() {
		return worksDuration;
	}
	public void setWorksDuration(String worksDuration) {
		this.worksDuration = worksDuration;
	}
	public String getWorksSpendTime() {
		return worksSpendTime;
	}
	public void setWorksSpendTime(String worksSpendTime) {
		this.worksSpendTime = worksSpendTime;
	}
	
}
