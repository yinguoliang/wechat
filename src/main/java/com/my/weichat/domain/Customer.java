package com.my.weichat.domain;

public class Customer {
	private String customerID;
	private String customerWechatID;
	private String customerName;
	private String customerCellPhone;
	private String customerEmail;
	private String customerAddr;
	private String customerDoor;
	private String customerRemark;
	public String getCustomerID() {
		return customerID;
	}
	public void setCustomerID(String customerID) {
		this.customerID = customerID;
	}
	public String getCustomerWechatID() {
		return customerWechatID;
	}
	public void setCustomerWechatID(String customerWechatID) {
		this.customerWechatID = customerWechatID;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getCustomerCellPhone() {
		return customerCellPhone;
	}
	public void setCustomerCellPhone(String customerCellPhone) {
		this.customerCellPhone = customerCellPhone;
	}
	public String getCustomerEmail() {
		return customerEmail;
	}
	public void setCustomerEmail(String customerEmail) {
		this.customerEmail = customerEmail;
	}
	public String getCustomerAddr() {
		return customerAddr;
	}
	public void setCustomerAddr(String customerAddr) {
		this.customerAddr = customerAddr;
	}
	public String getCustomerDoor() {
		return customerDoor;
	}
	public void setCustomerDoor(String customerDoor) {
		this.customerDoor = customerDoor;
	}
	public String getCustomerRemark() {
		return customerRemark;
	}
	public void setCustomerRemark(String customerRemark) {
		this.customerRemark = customerRemark;
	}
}
